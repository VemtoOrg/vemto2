import { default as pluralizer } from 'pluralize'

export default class ReservedKeywords {
    isReserved(word: string): boolean {
        word = word.toLowerCase()

        return this.getAll().includes(word)
    }

    getAll(): string[] {
        return this.getKeywords().concat(this.getPlurals())
    }

    getPlurals(): string[] {
        return this.getKeywords().map((keyword) => pluralizer.plural(keyword))
    }

    getKeywords() {
        return [
            'abstract',
            'and',
            'array',
            'as',
            'break',
            'callable',
            'case',
            'catch',
            'class',
            'clone',
            'const',
            'continue',
            'declare',
            'default',
            'die',
            'do',
            'echo',
            'else',
            'elseif',
            'empty',
            'enddeclare',
            'endfor',
            'endforeach',
            'endif',
            'endswitch',
            'endwhile',
            'eval',
            'exit',
            'extends',
            'final',
            'finally',
            'fn',
            'for',
            'foreach',
            'function',
            'global',
            'goto',
            'if',
            'implements',
            'include',
            'include_once',
            'instanceof',
            'insteadof',
            'interface',
            'isset',
            'list',
            'match',
            'namespace',
            'new',
            'or',
            'print',
            'private',
            'protected',
            'public',
            'require',
            'require_once',
            'return',
            'static',
            'switch',
            'throw',
            'trait',
            'try',
            'unset',
            'use',
            'var',
            'while',
            'xor',
        ]
    }

}